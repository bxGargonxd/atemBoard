#include "SwitcherMonitor.h"

// IIDs are defined in a way that prevents us from using CFEqual, so we overload the == operator
static inline bool operator== (const REFIID& iid1, const REFIID& iid2)
{
	return CFEqual(&iid1, &iid2);
}

SwitcherMonitor::SwitcherMonitor() :
	m_refCount(1)
{
}

HRESULT SwitcherMonitor::QueryInterface(REFIID iid, LPVOID *ppv)
{
	HRESULT result = S_OK;

	if (ppv == nullptr)
		return E_INVALIDARG;

	if (CFEqual(&iid, IUnknownUUID))
	{
		*ppv = this;
		AddRef();
	}
	else if (iid == IID_IBMDSwitcherCallback)
	{
		*ppv = static_cast<IBMDSwitcherCallback*>(this);
		AddRef();
	}
	else
	{
		*ppv = nullptr;
		result = E_NOINTERFACE;
	}

	return result;
}

ULONG SwitcherMonitor::AddRef()
{
	return ++m_refCount;
}

ULONG SwitcherMonitor::Release()
{
	ULONG newRefValue = --m_refCount;
	if (newRefValue == 0)
		delete this;

	return newRefValue;
}

HRESULT SwitcherMonitor::Notify(BMDSwitcherEventType eventType, BMDSwitcherVideoMode coreVideoMode)
{
	auto it = m_notifyHandlerFuncMap.find(eventType);
	if (it != m_notifyHandlerFuncMap.end() && it->second != nullptr)
		it->second();
	
	return S_OK;
}

void SwitcherMonitor::registerNotifyHandler(const BMDSwitcherEventType eventType, const NotifyEventFunc& func)
{
	if (func)
	{
		// Register callback
		m_notifyHandlerFuncMap[eventType] = func;
	}
	else
	{
		// If func is null, unregister callback by removing entry
		auto it = m_notifyHandlerFuncMap.find(eventType);
		if (it != m_notifyHandlerFuncMap.end())
			m_notifyHandlerFuncMap.erase(it);
	}
}

